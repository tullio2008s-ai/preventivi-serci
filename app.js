function sheetSaveClient(c){
  if(!gasReady())return;
  gasCall({action:"saveClient",client:c},null,function(e){setSheetStatus("⚠️ Client Sheets: "+e,true);});
}
function sheetDelClient(id){
  if(!gasReady())return;
  gasCall({action:"deleteClient",id:id},null,null);
}
function sheetLoadClients(cb){
  if(!gasReady()){if(cb)cb(false);return;}
  gasCall({action:"loadClients"},
    function(d){
      if(d.clients&&d.clients.length){
        // Merge with local — sheet wins for existing ids
        d.clients.forEach(function(sc){
          var found=false;
          for(var i=0;i<clienti.length;i++){
            if(clienti[i].id===sc.id){clienti[i]=sc;found=true;break;}
          }
          if(!found)clienti.push(sc);
        });
        lsSaveClienti();renderClienti();
        setSheetStatus("✅ "+clienti.length+" clienti caricati");
      }
      if(cb)cb(true);
    },
    function(e){if(cb)cb(false);}
  );
}

/* ── INIT ── */
function init(){
  try{var a=lsGet("gs_arc");archive=a?JSON.parse(a):[];}catch(x){archive=[];}
  try{var b=lsGet("gs_brands");brands=b?JSON.parse(b):JSON.parse(JSON.stringify(DEF_BRANDS));}catch(x){brands=JSON.parse(JSON.stringify(DEF_BRANDS));}
  clienti=lsGetClienti();
  updateConfigBtn();
  newPrev();renderArchive();renderCatalog();renderClienti();
  wireButtons();
  /* Auto-sync from Sheets on load */
  if(gasReady()){
    setTimeout(function(){
      sheetLoadAll(null);
      sheetLoadClients(null);
    },600);
  } else {
    setSheetStatus("⚙️ Configura Google Sheets per la sincronizzazione");
  }
}
if(document.readyState==="loading"){
  document.addEventListener("DOMContentLoaded",init);
}else{
  init();
}

