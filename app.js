/* ── IMPORT CLIENTI from CSV/Excel ── */
function importClienti(file){
  var ext=file.name.split(".").pop().toLowerCase();
  if(ext==="csv"){
    var reader=new FileReader();
    reader.onload=function(e){parseClientCSV(e.target.result);};
    reader.readAsText(file,"UTF-8");
  } else if(ext==="xlsx"||ext==="xls"){
    var reader2=new FileReader();
    reader2.onload=function(e){parseClientExcel(e.target.result);};
    reader2.readAsArrayBuffer(file);
  } else {
    alert("Formato non supportato. Usa CSV o Excel (.xlsx, .xls)");
  }
}

function parseClientCSV(text){
  var lines=text.split(/\r?\n/).filter(function(l){return l.trim();});
  if(lines.length<2){alert("Il file CSV sembra vuoto o ha solo l'intestazione.");return;}
  // Auto-detect separator
  var sep=lines[0].indexOf(";")>=0?";":(",");
  var headers=lines[0].split(sep).map(function(h){return h.trim().toLowerCase().replace(/"/g,"");});
  var imported=0,updated=0;
  for(var i=1;i<lines.length;i++){
    var cols=lines[i].split(sep).map(function(c){return c.trim().replace(/^"|"$/g,"");});
    var row={}; headers.forEach(function(h,idx){row[h]=cols[idx]||"";});
    var ragione=row["ragione sociale"]||row["ragionesociale"]||row["cliente"]||row["nome"]||row["company"]||"";
    if(!ragione)continue;
    var existing=null;
    for(var j=0;j<clienti.length;j++){
      if((clienti[j].ragione||"").toLowerCase()===ragione.toLowerCase()){existing=clienti[j];break;}
    }
    var nc={id:existing?existing.id:Date.now()+i,
      ragione:ragione,
      indirizzo:row["indirizzo"]||row["address"]||"",
      citta:row["città"]||row["citta"]||row["city"]||row["cap"]||"",
      piva:row["p.iva"]||row["piva"]||row["partita iva"]||row["cf"]||row["vat"]||"",
      referente:row["referente"]||row["contatto"]||row["contact"]||"",
      email:row["email"]||row["e-mail"]||row["mail"]||"",
      nPrev:existing?existing.nPrev:0,
      ultimoPrev:existing?existing.ultimoPrev:"",
      ultimoAggiornamento:new Date().toISOString().split("T")[0]};
    if(existing){Object.assign(existing,nc);updated++;}
    else{clienti.push(nc);imported++;}
    sheetSaveClient(nc);
  }
  lsSaveClienti();renderClienti();
  toast("👥 Importati: "+imported+" nuovi, "+updated+" aggiornati");
}

function parseClientExcel(buffer){
  // Requires SheetJS — load dynamically if needed
  if(typeof XLSX==="undefined"){
    var script=document.createElement("script");
    script.src="https://cdnjs.cloudflare.com/ajax/libs/xlsx/0.18.5/xlsx.full.min.js";
    script.onload=function(){doParseExcel(buffer);};
    document.head.appendChild(script);
  } else {
    doParseExcel(buffer);
  }
}
function doParseExcel(buffer){
  try{
    var wb=XLSX.read(new Uint8Array(buffer),{type:"array"});
    var ws=wb.Sheets[wb.SheetNames[0]];
    var csv=XLSX.utils.sheet_to_csv(ws);
    parseClientCSV(csv);
  }catch(e){alert("Errore lettura Excel: "+e.message);}
}

