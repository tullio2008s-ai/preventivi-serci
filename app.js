var GAS_URL = lsGet("gs_gasurl") || "";

function gasReady(){return GAS_URL && GAS_URL.indexOf("script.google.com")>=0;}

/* Call Apps Script Web App */
function gasCall(payload, onDone, onErr){
  if(!gasReady()){if(onErr)onErr("GAS_URL non configurato");return;}
  fetch(GAS_URL,{
    method:"POST",
    headers:{"Content-Type":"text/plain"},
    body:JSON.stringify(payload)
  })
  .then(function(r){return r.json();})
  .then(function(d){if(d.error)throw new Error(d.error);if(onDone)onDone(d);})
  .catch(function(e){if(onErr)onErr(e.message||String(e));});
}

/* Push single record to Sheets */
function sheetSave(rec){
  if(!gasReady())return;
  gasCall({action:"save",record:rec},
    function(){setSheetStatus("✅ Sincronizzato con Google Sheets");},
    function(e){setSheetStatus("⚠️ Sheets: "+e,true);}
  );
}

/* Delete from Sheets */
function sheetDelete(id){
  if(!gasReady())return;
  gasCall({action:"delete",id:id},null,function(e){setSheetStatus("⚠️ Delete: "+e,true);});
}

/* Load ALL from Sheets on startup */
function sheetLoadAll(cb){
  if(!gasReady()){if(cb)cb(false);return;}
  setSheetStatus("⏳ Caricamento da Google Sheets...");
  gasCall({action:"loadAll"},
    function(d){
      if(d.records&&d.records.length){
        archive=d.records;
        lsSet("gs_arc",JSON.stringify(archive));
        renderArchive();
        setSheetStatus("✅ "+archive.length+" preventivi caricati da Sheets");
      } else {
        setSheetStatus("✅ Connesso a Google Sheets (vuoto)");
      }
      if(cb)cb(true);
    },
    function(e){setSheetStatus("⚠️ Sheets non raggiungibile: "+e,true);if(cb)cb(false);}
  );
}

